function deepTr(x, dict) {
  if (typeof x === 'string') return dict[x] || x;
  if (Array.isArray(x)) return x.map((v) => deepTr(v, dict));
  if (x && typeof x === 'object') {
    const out = {};
    for (const [k, v] of Object.entries(x)) out[k] = deepTr(v, dict);
    return out;
  }
  return x;
}

